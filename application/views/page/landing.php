<script>
    var last_name;
    var first_name;
    var mobile;
    var address;
    var email;
    var subscribe_form;
    var data;
    var xhr;

    $(document).ready(init)

    function onSubmitSubscription()
    {
        var errorCount = 0;
        $('.errors').hide();
        $('#sucess-panel').hide();

        if(xhr && xhr.readyState != 4) {
            xhr.abort();
        }

        xhr = $.ajax({
            url: '<?php echo base_url('index.php/home/register')?>',
            type: 'GET',
            data: data,
            beforeSend: function() {
                $(':input').prop('disabled', true);
            },
            success: function(data){
                $.each(data, function (key, value){
//                        console.log(key);
                    if (value) {
                        errorCount++;
                        switch(key) {
                            case 'first_name_error':
                                $("#first_name_error").show();
                                break;
                            case 'last_name_error':
                                $("#last_name_error").show();
                                break;
                            case 'email_error':
                                $("#email_error").show();
                                break;
                            case 'mobile_error':
                                $("#mobile_error").show();
                                break;
                            case 'address_error':
                                $("#address_error").show();
                                break;
                            case 'email_exist_error':
                                $("#email_exist_error").show();
                                break;
                        }
                    }
                });



                if(!errorCount) {
                    console.log(errorCount);
                    $('input').val('');
                    $('#sucess-panel').slideDown('fast');
                }
//                    else {
//                        $('#sucess-panel').slideDown('fast');
//                    }
            },
            complete: function(){
                $(':input').prop('disabled', false);
            }
        });
    }

    function hideErrors()
    {
        $("#first_name_error").hide();
        $("#last_name_error").hide();
        $("#email_error").hide();
        $("#mobile_error").hide();
        $("#address_error").hide();
        $("#email_exist_error").hide();

    }

    function onSendSubcription(e)
    {
        e.preventDefault();

        data = {};
        data = {
            first_name : first_name.val(),
            last_name : last_name.val(),
            email : email.val(),
            address : address.val(),
            mobile_number : mobile.val(),
        };
        onSubmitSubscription()
    }

    function init()
    {
        $('.errors').hide();
        $('#sucess-panel').hide();

        last_name = $('#lname');
        first_name = $('#fname');
        mobile = $('#mobile');
        email = $('#email');
        address = $('#address');
        subscribe_form = $('#submit');
        subscribe_form.on('click', onSendSubcription);
    }
</script>
